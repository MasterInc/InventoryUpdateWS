package com.ivend.client.objects;

public class BatchsItem {

	String UID;
	String message;
	boolean generateEnterpriseEvent;
	String enterpriseName;
	String userFieldList;
	String batchNumber ;
	String quantity ;
	String baseType ;
	String baseEntry ;
	String lineNumber ;
	String expiryDate ;
	String productionDate ;
	String inDate ;
	String price ;
	
	
	public String getUID() {
		return UID;
	}
	public void setUID(String uID) {
		UID = uID;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public boolean isGenerateEnterpriseEvent() {
		return generateEnterpriseEvent;
	}
	public void setGenerateEnterpriseEvent(boolean generateEnterpriseEvent) {
		this.generateEnterpriseEvent = generateEnterpriseEvent;
	}
	public String getEnterpriseName() {
		return enterpriseName;
	}
	public void setEnterpriseName(String enterpriseName) {
		this.enterpriseName = enterpriseName;
	}
	public String getUserFieldList() {
		return userFieldList;
	}
	public void setUserFieldList(String userFieldList) {
		this.userFieldList = userFieldList;
	}
	public String getBatchNumber() {
		return batchNumber;
	}
	public void setBatchNumber(String batchNumber) {
		this.batchNumber = batchNumber;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	public String getBaseType() {
		return baseType;
	}
	public void setBaseType(String baseType) {
		this.baseType = baseType;
	}
	public String getBaseEntry() {
		return baseEntry;
	}
	public void setBaseEntry(String baseEntry) {
		this.baseEntry = baseEntry;
	}
	public String getLineNumber() {
		return lineNumber;
	}
	public void setLineNumber(String lineNumber) {
		this.lineNumber = lineNumber;
	}
	public String getExpiryDate() {
		return expiryDate;
	}
	public void setExpiryDate(String expiryDate) {
		this.expiryDate = expiryDate;
	}
	public String getProductionDate() {
		return productionDate;
	}
	public void setProductionDate(String productionDate) {
		this.productionDate = productionDate;
	}
	public String getInDate() {
		return inDate;
	}
	public void setInDate(String inDate) {
		this.inDate = inDate;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	

	
}
